package ex3;

public class Personagem {
	
	String raca;
	double vida;
	int pontAtaque;
	int capacDefesa;
	boolean premium;
	
	public Personagem(String raca,double vida,int pontAtaque,int capacDefesa,boolean premium) {
		
		this.raca = raca;
		this.vida = vida;
		this.pontAtaque = pontAtaque;
		this.capacDefesa = capacDefesa;
		this.premium = premium;
		
	}
	
	

}
